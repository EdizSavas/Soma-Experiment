# Kategori istatistikleri
def kategori_istatistikleri():
    """Veri setindeki kategori dağılımını gösterir"""
    from collections import Counter
    
    kategoriler = [kayit["kategori"] for kayit in HASTA_KAYITLARI]
    sayilar = Counter(kategoriler)
    
    print("\n" + "="*60)
    print("📊 HASTA KAYITLARI İSTATİSTİKLERİ")
    print("="*60)
    print(f"\nToplam Kayıt: {len(HASTA_KAYITLARI)}")
    print(f"\nKategori Dağılımı:")
    print("-"*60)
    
    for kategori, sayi in sorted(sayilar.items(), key=lambda x: x[1], reverse=True):
        print(f"  {kategori:.<30} {sayi:>3} kayıt")
    
    print("="*60)

